; SUBROUTINE TO REPLACE SWAP INSTRUCTION

ORG 00H

MOV R1, #6BH
MOV A, R1
MOV B, A
LCALL M_SWAP

DISPLAY: MOV P0, B
LCALL DELAY
MOV P0, A
LCALL DELAY
SJMP DISPLAY



ORG 200H

M_SWAP: RR A
RR A
RR A
RR A
RET

ORG 300H

DELAY: 
MOV R5, 0x50
HERE1: MOV R6, 0XFF
HERE2: MOV R7, 0XFF
HERE3: DJNZ R7, HERE3
DJNZ R6, HERE2
DJNZ R5, HERE1
RET

END