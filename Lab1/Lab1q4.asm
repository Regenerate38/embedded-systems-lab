; SUBROUTINE TO DIVIDE BY LOOPING AND SUCCESSIVE ADDITION

ORG 00H

MOV 3EH, #0xAF
MOV A, 3EH

DIVI:
SUBB A, #0x12
JC SKIP
INC B
JMP DIVI
SKIP: ADD A, #0x12  ; CARRY MEANS -VE MA GAYO SO PHERI JODDA REQUIRED REMAINDER AAUNE BHO


MOV R4, B
MOV R5, A

DISP:
MOV P1, R4
LCALL DELAY
MOV P1, R5
LCALL DELAY
SJMP DISP


ORG 300H

DELAY:
MOV R1, 0x50
HERE1: MOV R6, 0XFF
HERE2: MOV R7, 0XFF
HERE3: DJNZ R7, HERE3
DJNZ R6, HERE2
DJNZ R1, HERE1
RET

END