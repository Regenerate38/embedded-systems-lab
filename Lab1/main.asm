; SUBROUTINE TO MULTIPLY BY LOOPING AND SUCCESSIVE ADDITION

ORG 00H


MOV 22H, #0xFF
MOV 15H, #0xDE

MOV R0, 22H
MOV R1, 15H

MULT:
ADD A, R0
JNC SKIP
INC B
SKIP: DJNZ R1, MULT

MOV 19H, A
MOV 1AH, B

DISP:
MOV P0, 19H
LCALL DELAY
MOV P0, 1AH
LCALL DELAY
SJMP DISP

ORG 300H

DELAY:
MOV R5, 0x50
HERE1: MOV R6, 0XFF
HERE2: MOV R7, 0XFF
HERE3: DJNZ R7, HERE3
DJNZ R6, HERE2
DJNZ R5, HERE1
RET

END
