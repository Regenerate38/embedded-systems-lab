; CODE FOR SINGLE DECIMAL DIGIT COUNTER

ORG 00H
; DATA VALUE TO DISPLAY TO PORT FOR SEVEN SEGMENT DISPLAY FOR 0 TO 9 RESPECTIVELY
NUMS: DB 0X3F, 0X06, 0X5B, 0X4F,0X66, 0X6D, 0X7D, 0X07, 0X7F, 0X4F 

MOV DPTR, #NUMS
MOV P2, #0XFE ; EQUIVALENT TO C'S HAVING P2^0 AS 0
RESET: MOV R0, #0X00
L1: MOV A, R0
MOVC A, @A+DPTR ; TO GET THE SEVEN SEGMENT DISPLAY VALUE OF OUR REQUIRED NUMBER
MOV P0, A
LCALL DELAY
INC R0
CJNE R0, #0X0A, L1
DEC R0

L2:DEC R0
MOV A, R0
MOVC A, @A+DPTR
MOV P0,A
LCALL DELAY
CJNE R0, #0X01, L2
JMP RESET

ORG 300H

DELAY:
MOV R1, 0x50
HERE1: MOV R6, 0XFF
HERE2: MOV R7, 0XFF
HERE3: DJNZ R7, HERE3
DJNZ R6, HERE2
DJNZ R1, HERE1
RET

END